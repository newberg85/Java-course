package fundamentos;
public class Aula05EstruturasDeDecisao {

    public static void main(String[] args) {
        int valor = 8;

        if(valor == 8){
            System.out.println("O valor é igual a 8");
        } else if(valor > 8){
            System.out.println("O valor é maior que 8");
        } else {
            System.out.println("O valor é menor que 8");
        }
    }

}
