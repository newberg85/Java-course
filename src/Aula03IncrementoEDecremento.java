public class Aula03IncrementoEDecremento {

    public static void main(String[] args) {
        int num1, num2;

        num1 = 6;
        num2 = 6;

        System.out.println(num1);
        System.out.println(num2);

        num1++; //num1 = num1 + 1
        ++num2; //num2 = num2 + 2

        System.out.println(num1);
        System.out.println(num2);
    }

}
